"""Extra visualization + diagnostics for rho_curve finite-size scaling.

Why this exists
- `analysis.rho_curve_scaling` already writes summary/fit and a few basic plots.
- For interpreting non-monotone k50(N) shapes and verifying data quality, we want
  additional figures (overlay curves, zoom-in crossings, residuals, heatmaps,
  and (k-k_inf)*N^beta style collapse attempts) plus a single per-N diagnostics CSV.

Design constraints
- Lives in `analysis/` and does NOT import `simulation/`.
- Stdlib-only for data plumbing; plotting uses optional `matplotlib`.
- Inputs are the same sweep CSVs used by scaling/report (ideally extracted from
  a compose script generated by `analysis.compose_rho_curve_commands`).

Typical usage (iter8 example)

./venv/bin/python -m analysis.rho_curve_viz \
  --prefix rho_curve_merged_with_lowband_clean_plus_prov2_S120_iter8_20260310_204815 \
  --summary outputs/analysis/rho_curve/<prefix>_summary.csv \
  --fit-json outputs/analysis/rho_curve/<prefix>_k50_fit.json \
  --compose outputs/_compose_cmds_<prefix>.sh \
  --outdir outputs/analysis/rho_curve \
  --eps 0.02 \
  --betas 0.5,0.8,1.0,1.5

Outputs
- PNGs under <outdir>/figs/<prefix>_*.png
- Diagnostics CSV: <outdir>/<prefix>_diagnostics.csv
"""

from __future__ import annotations

import argparse
import csv
import json
import math
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Dict, Iterable, List, Optional, Sequence, Tuple


@dataclass(frozen=True)
class SummaryRow:
	players: int
	k50: Optional[float]
	k50_bayes_mean: Optional[float]
	k50_bayes_ci_low: Optional[float]
	k50_bayes_ci_high: Optional[float]
	k_min: Optional[float]
	k_max: Optional[float]
	corr: Optional[float]
	rho_m1_50: Optional[float]
	k50_censored_low: bool


@dataclass(frozen=True)
class SweepStats:
	players: int
	score: float
	monov: int
	cross: int
	signchg: int
	points: int


def _try_float(v: object) -> Optional[float]:
	try:
		if v is None:
			return None
		s = str(v).strip()
		if s == "":
			return None
		return float(s)
	except Exception:
		return None


def _try_int(v: object) -> Optional[int]:
	try:
		if v is None:
			return None
		s = str(v).strip()
		if s == "":
			return None
		return int(float(s))
	except Exception:
		return None


def _read_summary(path: Path) -> List[SummaryRow]:
	rows: List[SummaryRow] = []
	with path.open("r", newline="") as f:
		r = csv.DictReader(f)
		for row in r:
			players = _try_int(row.get("players"))
			if players is None:
				continue
			rows.append(
				SummaryRow(
					players=int(players),
					k50=_try_float(row.get("k50")),
					k50_bayes_mean=_try_float(row.get("k50_bayes_mean")),
					k50_bayes_ci_low=_try_float(row.get("k50_bayes_ci_low")),
					k50_bayes_ci_high=_try_float(row.get("k50_bayes_ci_high")),
					k_min=_try_float(row.get("k_min")),
					k_max=_try_float(row.get("k_max")),
					corr=_try_float(row.get("corr_rho_minus_1_p_level_3")),
					rho_m1_50=_try_float(row.get("rho_minus_1_50")) or _try_float(row.get("rho_m1_50")),
					k50_censored_low=str(row.get("k50_censored_low") or "").strip().lower() in ("1", "true", "yes"),
				)
			)
	rows.sort(key=lambda x: x.players)
	return rows


def _extract_in_paths_from_compose(compose_sh: Path) -> List[Path]:
	text = compose_sh.read_text(encoding="utf-8", errors="replace")
	paths = re.findall(r"--in '([^']+)'", text)
	out: List[Path] = []
	seen: set[str] = set()
	for p in paths:
		pp = str(p).strip()
		if not pp:
			continue
		if pp in seen:
			continue
		seen.add(pp)
		out.append(Path(pp))
	return out


def _read_sweeps(paths: Sequence[Path]) -> Dict[int, List[Tuple[float, float]]]:
	by: Dict[int, List[Tuple[float, float]]] = {}
	for path in paths:
		if not path.exists():
			continue
		with path.open("r", newline="") as f:
			r = csv.DictReader(f)
			for row in r:
				players = _try_int(row.get("players"))
				k = _try_float(row.get("selection_strength"))
				p3 = _try_float(row.get("p_level_3"))
				if players is None or k is None or p3 is None:
					continue
				by.setdefault(int(players), []).append((float(k), float(p3)))
	# sort & de-dup by k (keep last)
	for n, pts in list(by.items()):
		pts.sort(key=lambda kv: kv[0])
		uniq: Dict[float, float] = {}
		for k, p3 in pts:
			uniq[float(k)] = float(p3)
		by[n] = sorted(uniq.items(), key=lambda kv: kv[0])
	return by


def _sweep_stats(by: Dict[int, List[Tuple[float, float]]], *, target: float, eps: float) -> Dict[int, SweepStats]:
	out: Dict[int, SweepStats] = {}
	for n, pts in by.items():
		y = [p[1] for p in pts]
		if len(y) < 5:
			continue

		monov = 0
		dys: List[float] = []
		for i in range(len(y) - 1):
			dy = float(y[i + 1]) - float(y[i])
			dys.append(dy)
			if dy < -float(eps):
				monov += 1

		signs: List[int] = []
		for dy in dys:
			if abs(float(dy)) <= float(eps):
				continue
			signs.append(1 if dy > 0.0 else -1)
		sc = 0
		for i in range(len(signs) - 1):
			if signs[i] != signs[i + 1]:
				sc += 1

		cross = 0
		for i in range(len(y) - 1):
			a = float(y[i]) - float(target)
			b = float(y[i + 1]) - float(target)
			if a == 0.0:
				continue
			if (a < 0.0 and b > 0.0) or (a > 0.0 and b < 0.0):
				cross += 1

		score = (2.0 * float(monov)) + (1.0 * float(cross)) + (0.25 * float(sc))
		out[int(n)] = SweepStats(players=int(n), score=float(score), monov=int(monov), cross=int(cross), signchg=int(sc), points=int(len(y)))
	return out


def _summary_score(row: SummaryRow) -> float:
	# Mirror analysis.select_noisy_players summary heuristic (for documentation parity).
	s = 0.0
	if row.corr is None:
		s += 1.0
	else:
		s += max(0.0, 1.0 - float(row.corr))
	if row.k50 is None:
		s += 1.0
	if row.k50_censored_low:
		s += 1.0
	if row.rho_m1_50 is not None:
		s += 0.25 * (1.0 / (1e-6 + abs(float(row.rho_m1_50))))
	return float(s)


def _ensure_matplotlib():
	try:
		import matplotlib  # noqa: F401
	except ModuleNotFoundError as e:
		raise SystemExit(
			"matplotlib 未安裝：此工具需要繪圖。\n"
			"請執行：./venv/bin/python -m pip install matplotlib\n"
			"或在 requirements.txt 取消註解 matplotlib（optional）。"
		) from e


def _plot_overlay_p3_vs_k(by: Dict[int, List[Tuple[float, float]]], *, outpath: Path, title: str) -> None:
	_ensure_matplotlib()
	import matplotlib.pyplot as plt

	plt.figure(figsize=(7, 4.2))
	for n in sorted(by.keys()):
		pts = by[n]
		x = [p[0] for p in pts]
		y = [p[1] for p in pts]
