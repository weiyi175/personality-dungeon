def main(argv: Optional[Sequence[str]] = None) -> None:
	p = argparse.ArgumentParser(description="Extra rho_curve diagnostics plots + per-N metrics")
	p.add_argument("--prefix", required=True, help="Prefix used for naming outputs")
	p.add_argument("--summary", required=True, help="Path to *_summary.csv")
	p.add_argument("--fit-json", default=None, help="Path to *_k50_fit.json (optional)")
	p.add_argument("--compose", default=None, help="Compose .sh produced by analysis.compose_rho_curve_commands (optional)")
	p.add_argument("--in", dest="inputs", action="append", default=[], help="Sweep CSV path (repeatable); alternative to --compose")
	p.add_argument("--outdir", default="outputs/analysis/rho_curve", help="Output directory (default: outputs/analysis/rho_curve)")
	p.add_argument("--eps", type=float, default=0.02, help="Eps tolerance for monotonicity/sign checks (default: 0.02)")
	p.add_argument("--target", type=float, default=0.5, help="Crossing target for sweeps stats (default: 0.5)")
	p.add_argument("--betas", default="0.5,0.8,1.0,1.5", help="Comma-separated beta list for collapse (default: 0.5,0.8,1.0,1.5)")
	p.add_argument(
		"--k-inf",
		type=float,
		default=None,
		help="Optional override for k_inf used in collapse plots/scores (default: take from --fit-json).",
	)
	p.add_argument("--zoom-band", type=float, default=0.12, help="Global k window half-width for zoom plot (default: 0.12)")
	p.add_argument(
		"--collapse-pmin",
		type=float,
		default=0.1,
		help="Only use points with P(Level3) >= this for collapse scoring (default: 0.1)",
	)
	p.add_argument(
		"--collapse-pmax",
		type=float,
		default=0.9,
		help="Only use points with P(Level3) <= this for collapse scoring (default: 0.9)",
	)
	p.add_argument(
		"--collapse-score-method",
		choices=["xbins", "xgrid", "ygrid"],
		default="xbins",
		help="Collapse score method: xbins (legacy), xgrid (robust), or ygrid (experimental) (default: xbins)",
	)
	p.add_argument("--collapse-x-grid-n", type=int, default=41, help="For xgrid: number of x grid points (default: 41)")
	p.add_argument(
		"--collapse-y-grid",
		default=None,
		help="For ygrid method: y values as lo:hi:step or comma list (default: use collapse-pmin..collapse-pmax step 0.02)",
	)
	args = p.parse_args(list(argv) if argv is not None else None)

	prefix = str(args.prefix)
	outdir = Path(str(args.outdir))
	figdir = outdir / "figs"
	figdir.mkdir(parents=True, exist_ok=True)

	summary_path = Path(str(args.summary))
	summary = _read_summary(summary_path)
	centers = {r.players: float(r.k50) for r in summary if r.k50 is not None}

	fit: Optional[dict] = None
	if args.fit_json:
		fit_path = Path(str(args.fit_json))
		if fit_path.exists():
			fit = json.loads(fit_path.read_text(encoding="utf-8"))

	in_paths: List[Path] = []
	if args.compose:
		in_paths = _extract_in_paths_from_compose(Path(str(args.compose)))
	else:
		in_paths = [Path(x) for x in (args.inputs or [])]
	if not in_paths:
		raise SystemExit("需要提供 --compose 或至少一個 --in")

	by = _read_sweeps(in_paths)
	stats = _sweep_stats(by, target=float(args.target), eps=float(args.eps))

	# figures
	_plot_overlay_p3_vs_k(by, outpath=figdir / f"{prefix}_pL3_vs_k_overlay.png", title="P(Level3) vs k (overlay; all N)")
	_plot_overlay_zoom(
		by,
		centers=centers,
		band=float(args.zoom_band),
		outpath=figdir / f"{prefix}_pL3_vs_k_overlay_zoom.png",
		title="P(Level3) vs k (zoom around crossings)",
	)

	_plot_heatmap(by, outpath=figdir / f"{prefix}_pL3_heatmap_k_by_N.png", title="Heatmap: P(Level3) over (N, k)")

	_plot_k50_transforms(summary, fit=fit, outdir=figdir, prefix=prefix)

	# collapse attempts
	betas = []
	for part in str(args.betas).split(","):
		part = part.strip()
		if not part:
			continue
		try:
			betas.append(float(part))
		except Exception:
			continue

	k_inf: float | None = None
	if args.k_inf is not None:
		k_inf = float(args.k_inf)
	elif fit and "k_inf" in fit and fit["k_inf"] is not None:
		k_inf = float(fit["k_inf"])
	if k_inf is not None and betas:
		scores: dict[str, float] = {}
		score_method = str(args.collapse_score_method)
		if score_method == "ygrid":
			if args.collapse_y_grid is None:
				y_grid = _parse_grid_spec(f"{float(args.collapse_pmin)}:{float(args.collapse_pmax)}:0.02")
				y_grid_spec = f"{float(args.collapse_pmin)}:{float(args.collapse_pmax)}:0.02"
			else:
				y_grid = _parse_grid_spec(str(args.collapse_y_grid))
				y_grid_spec = str(args.collapse_y_grid)
		else:
			y_grid = []
			y_grid_spec = None
		for b in betas:
			_plot_collapse_k(
				by,
				k_inf=float(k_inf),
				beta=float(b),
				outpath=figdir / f"{prefix}_collapse_k_kInf_{k_inf:.4f}_beta_{b:g}.png",
				title=f"Collapse attempt: (k-k_inf)N^beta (k_inf={k_inf:.4f}, beta={b:g})",
			)
			if score_method == "ygrid":
				s = _collapse_score_ygrid(
					by,
					k_inf=float(k_inf),
					beta=float(b),
					y_grid=y_grid,
					p3_min=float(args.collapse_pmin),
					p3_max=float(args.collapse_pmax),
				)
			elif score_method == "xgrid":
				s = _collapse_score_xgrid(
					by,
					k_inf=float(k_inf),
					beta=float(b),
					x_grid_n=int(args.collapse_x_grid_n),
					p3_min=float(args.collapse_pmin),
					p3_max=float(args.collapse_pmax),
				)
			else:
				s = _collapse_score(
					by,
					k_inf=float(k_inf),
					beta=float(b),
					p3_min=float(args.collapse_pmin),
					p3_max=float(args.collapse_pmax),
				)
			if s is not None:
				scores[f"{float(b):g}"] = float(s)
		if scores:
			best_beta = min(scores.items(), key=lambda kv: kv[1])[0]
			out_json = figdir / f"{prefix}_collapse_scores_kInf_{k_inf:.4f}.json"
			out_json.write_text(
				json.dumps(
					{
						"k_inf": float(k_inf),
						"scores": scores,
						"best_beta": best_beta,
						"score_method": score_method,
						"collapse_pmin": float(args.collapse_pmin),
						"collapse_pmax": float(args.collapse_pmax),
						"x_grid_n": int(args.collapse_x_grid_n),
						"y_grid_spec": y_grid_spec,
						"y_grid": y_grid if score_method == "ygrid" else None,
					},
					indent=2,
				),
				encoding="utf-8",
			)
			print(f"Collapse scores: wrote {out_json} (best_beta={best_beta})")

	# per-N diagnostics CSV
	_write_diagnostics_csv(
		outpath=outdir / f"{prefix}_diagnostics.csv",
		summary=summary,
		sweep_stats=stats,
		k50_fit=fit,
		eps=float(args.eps),
		target=float(args.target),
	)

	print(f"Wrote figures under: {figdir}")
	print(f"Wrote diagnostics: {outdir / f'{prefix}_diagnostics.csv'}")


if __name__ == "__main__":
	main()
